package com.company;

public class Odev7 {
    public static void main(String[] args) {
        for (int i = 0 ; i < 5 ; i++) {
            System.out.println((i+1) + ". FOR" + "Mustafa Dağcan ŞENTÜRK");
        }
        int j = 0;
        while (j < 5) {
            System.out.println((j+1) + ". WHILE" + "Mustafa Dağcan ŞENTÜRK");
            j++;
        }
        int k = 0;
        do {
            System.out.println((k+1) + ". DO WHILE" + "Mustafa Dağcan ŞENTÜRK");
            k++;
        } while (k < 5);

    }
}
